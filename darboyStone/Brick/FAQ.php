		  <li class="boldBullet">What is cultured stone? </li>
		  <li class="noBullet">Cultured stone is a man-made concrete product formed and colored to imitate natural 
		  	stone. There are numerous colors, shapes, and sizes available. The advantage of cultured stone is that 
		  	it is thin and fairly lightweight compared to natural stone which allows for quicker application as well 
		  	as the elimination of the need for a brick/stone ledge. </li>
		  <li class="boldBullet">How is cultured stone applied? </li>
		  <li class="noBullet">A weather barrier and wire lath is applied to the surface that the stone will be 
		  	installed. A thin coat of mortar (called a scratch coat) is applied to the surface and allowed to set. 
		  	The stone is then applied to the wall. After the stone is applied the joints are finished with a grout 
		  	bag and finishing tool. </li>
		  <li class="boldBullet">Is there any type of warranty coverage for cultured stone? </li>
		  <li class="noBullet">Warranty coverage averages from 30-50 yrs depending on the manufacturer. This covers 
		  	the integrity of the stone itself against cracking or discoloration. The warranty does not cover breakage
		  	 due to improper installation. </li>
		  <li class="boldBullet">Can cultured stone be installed outside? </li>
